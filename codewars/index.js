function scramble(str1, str2) {
	const charCount = {};

	for (let char of str1) {
		charCount[char] = (charCount[char] || 0) + 1;
	}

	for (let char of str2) {
		if (charCount[char] && charCount[char] > 0) {
			charCount[char]--;
		} else {
			return false;
		}
	}

	return true;
}

